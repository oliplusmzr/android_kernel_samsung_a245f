static void check_stream_is_on(void)
{
	int i = 0;
	UINT32 framecnt;

	for (i = 0; i < 100; i++) {
		framecnt = read_cmos_sensor(0x0005);
		if (framecnt != 0xFF) {
			LOG_INF("stream is  on, %d\n", framecnt);
			break;
		}
		LOG_INF("stream is not on %d\n", framecnt);
		mdelay(1);
	}
}

